package com.callor.var;

public class VarF {
	public static void main(String[] args) {
		int num1 = 30;
		int num2 = 43;
		
		//데이터 type이 true, false로만 나타나는 것을 
		//boolean type라 한다.
		boolean bEven1 = num1 % 2 == 0;
		boolean bEven2 = num2 % 2 == 0;

		System.out.println(bEven1);
		System.out.println(bEven2);

	}
}
